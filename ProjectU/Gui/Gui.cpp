{
	SetWindowPos(window, HWND_TOPMOST, 0, 0, 0, 0, SWP_NOMOVE | SWP_NOSIZE);
	ImGui::SetNextWindowPos({ 0, 0 });
	ImGui::SetNextWindowSize({ WIDTH, HEIGHT });
	ImGui::Begin(
		xorstr_("Project U"),
		&isRunning,
		ImGuiWindowFlags_NoResize |
		ImGuiWindowFlags_NoSavedSettings |
		ImGuiWindowFlags_NoCollapse |
		ImGuiWindowFlags_NoMove
	);

	ImGui::Checkbox(xorstr_("Enable Clicker (LMB)"), &Globals::clicker);
	if (Globals::cpsRange)
		ImGui::SliderFloat(xorstr_("Max Cps"), &Globals::upperCps, Globals::lowerCps, 25.f);
	else
		ImGui::SliderFloat(xorstr_("CPS"), &Globals::cps, 5.f, 15.f, xorstr_("%.3f"));
	ImGui::Checkbox(xorstr_("CPS Range"), &Globals::cpsRange);
	if (Globals::cpsRange)
		ImGui::SliderFloat(xorstr_("Min CPS"), &Globals::lowerCps, 0.f, Globals::upperCps);

	if (!Globals::clickeroff && Globals::clicker)
		ImGui::TextColored(ImVec4{ 0.098,0.69,0.184,1 }, xorstr_("Clicker Enabled"));
	else
		ImGui::TextColored(ImVec4{ 0.902,0.263,0.263,1 }, xorstr_("Clicker Disabled"));

	ImGui::Checkbox(xorstr_("Hide from Capture (BETA)"), &Globals::hide);


	if (ImGui::Button("Pop"))
		Core::Pop();
	ImGui::Text(Globals::DEBUG);
	//ImGui::ColorEdit4("Glow Color", globals::glowColor);

	ImGui::End();
}

